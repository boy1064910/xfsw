package com.ptmind.datadeck.entity.datasource.field;

public class FieldParseSetting {

	String idKey;
	String nameKey;
	String groupKey;
	String dataTypeKey;
	String typeKey;
	String descriptionKey;
	String allowedSegmentsKey;
	
	public String getGroupKey() {
		return groupKey;
	}
	public void setGroupKey(String groupKey) {
		this.groupKey = groupKey;
	}
	public String getDataTypeKey() {
		return dataTypeKey;
	}
	public void setDataTypeKey(String dataTypeKey) {
		this.dataTypeKey = dataTypeKey;
	}
	public String getTypeKey() {
		return typeKey;
	}
	public void setTypeKey(String typeKey) {
		this.typeKey = typeKey;
	}
	public String getDescriptionKey() {
		return descriptionKey;
	}
	public void setDescriptionKey(String descriptionKey) {
		this.descriptionKey = descriptionKey;
	}
	public String getAllowedSegmentsKey() {
		return allowedSegmentsKey;
	}
	public void setAllowedSegmentsKey(String allowedSegmentsKey) {
		this.allowedSegmentsKey = allowedSegmentsKey;
	}
	public String getIdKey() {
		return idKey;
	}
	public void setIdKey(String idKey) {
		this.idKey = idKey;
	}
	public String getNameKey() {
		return nameKey;
	}
	public void setNameKey(String nameKey) {
		this.nameKey = nameKey;
	}
}
